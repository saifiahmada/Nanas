package com.ipi.nanas;

public class Semangka {
	
	private String semangka;
	
	private String warna;
	
	private String rasa;

	public String getSemangka() {
		return semangka;
	}

	public void setSemangka(String semangka) {
		this.semangka = semangka;
	}

	public String getWarna() {
		return warna;
	}

	public void setWarna(String warna) {
		this.warna = warna;
	}

	public String getRasa() {
		return rasa;
	}

	public void setRasa(String rasa) {
		this.rasa = rasa;
	}
	
}
