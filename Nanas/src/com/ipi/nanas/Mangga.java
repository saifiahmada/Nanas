package com.ipi.nanas;

public class Mangga {
	
	private String mangga;
	
	private String warna;
	
	private String rasa;

	public String getMangga() {
		return mangga;
	}

	public void setMangga(String mangga) {
		this.mangga = mangga;
	}

	public String getWarna() {
		return warna;
	}

	public void setWarna(String warna) {
		this.warna = warna;
	}

	public String getRasa() {
		return rasa;
	}

	public void setRasa(String rasa) {
		this.rasa = rasa;
	}
	
}
