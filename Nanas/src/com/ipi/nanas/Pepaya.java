package com.ipi.nanas;

public class Pepaya {
	
	private String pepaya;
	
	private String warna;
	
	private String rasa;

	public String getPepaya() {
		return pepaya;
	}

	public void setPepaya(String pepaya) {
		this.pepaya = pepaya;
	}

	public String getWarna() {
		return warna;
	}

	public void setWarna(String warna) {
		this.warna = warna;
	}

	public String getRasa() {
		return rasa;
	}

	public void setRasa(String rasa) {
		this.rasa = rasa;
	}
	
	

}
