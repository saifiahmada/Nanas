package com.ipi.nanas;

public class Kedondong {
	
	private String kedondong;
	
	private String warna;
	
	private String rasa;

	public String getKedondong() {
		return kedondong;
	}

	public void setKedondong(String kedondong) {
		this.kedondong = kedondong;
	}

	public String getWarna() {
		return warna;
	}

	public void setWarna(String warna) {
		this.warna = warna;
	}

	public String getRasa() {
		return rasa;
	}

	public void setRasa(String rasa) {
		this.rasa = rasa;
	}
	
}
